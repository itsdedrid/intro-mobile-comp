function changeContent(pet) {
    const content = document.getElementById("content");

    if (pet === "dog") {
        content.innerHTML = `
            <h1>สุนัข (Dog)</h1>
            <p>สุนัขเป็นสัตว์เลี้ยงที่ซื่อสัตย์และเป็นเพื่อนที่ดีของมนุษย์</p>
            <p>พวกมันสามารถฝึกให้ทำงานต่างๆ ได้ เช่น เฝ้าบ้าน หรือช่วยเหลือผู้พิการ</p>
        `;
    } else if (pet === "cat") {
        content.innerHTML = `
            <h1>แมว (Cat)</h1>
            <p>แมวเป็นสัตว์เลี้ยงยอดนิยมที่มีนิสัยเป็นอิสระและรักความสงบ</p>
            <p>พวกมันมักจะใช้เวลาไปกับการนอนและชอบเล่นของเล่นเล็กๆ</p>
        `;
    } else if (pet === "rabbit") {
        content.innerHTML = `
            <h1>กระต่าย (Rabbit)</h1>
            <p>กระต่ายเป็นสัตว์เลี้ยงที่น่ารักและอ่อนโยน</p>
            <p>พวกมันต้องการการดูแลที่ดี เช่น อาหารที่เหมาะสมและพื้นที่ให้วิ่งเล่น</p>
        `;
    }
}